import java.util.*;
public class whileSecond {

	public static void main(String[] args) {
		Scanner ws= new Scanner(System.in);
		int i,n;
		System.out.println("Enter Number upto which you want to print:-");
		n=ws.nextInt();
		i=1;
		while(i<=n)
		{
			System.out.println(i);
			i=i+1;
		}
		

	}

}
